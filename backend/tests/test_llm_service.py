import pytest
from unittest.mock import AsyncMock, MagicMock
from app.services.llm_service import generate_outreach_draft, self_reflect_draft, MAX_OUTPUT_LENGTH

@pytest.mark.asyncio
async def test_generate_outreach_draft(mock_openai):
    # Set up mock response
    mock_choice = MagicMock()
    mock_choice.message.content = "   This is a mock draft message.   "
    mock_response = MagicMock()
    mock_response.choices = [mock_choice]
    mock_openai.return_value = mock_response
    
    prompt = "Find computer hardware vendors"
    analysis_summary = "Option A is $500. Option B is $600."
    selected_vendor = {"name": "Test Vendor", "location": "Bangalore"}
    
    result = await generate_outreach_draft(prompt, analysis_summary, selected_vendor)
    assert result == "This is a mock draft message."
    
    # Verify mock call parameters
    mock_openai.assert_called_once()
    call_args = mock_openai.call_args[1]
    assert call_args["messages"][1]["content"] is not None

