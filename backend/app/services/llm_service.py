from typing import Optional
from openai import AsyncOpenAI
from app.core import config
from app.core.logger import get_logger

logger = get_logger("services.llm")

# Initialize centrally once using OPENAI_API_KEY
client = AsyncOpenAI(api_key=config.OPENAI_API_KEY)

# Maximum allowed output length for defensive truncation
MAX_OUTPUT_LENGTH = 2000

async def generate_outreach_draft(prompt: str, analysis_summary: str, selected_vendor: Optional[dict] = None) -> str:
    """
    Generates a professional and concise vendor outreach message using the user's
    task prompt and analysis summary for context.
    """
    logger.info("Draft generation started")
    
    location_context = (
        f"Procurement region:\n"
        f"City: {config.DEFAULT_CITY}\n"
        f"Locality: {config.DEFAULT_LOCALITY}\n"
        f"Pincode: {config.DEFAULT_PINCODE}"
    )

    vendor_context = ""
    if selected_vendor:
        vendor_context = (
            f"Preferred vendor:\n"
            f"{selected_vendor['name']} located in {selected_vendor['location']}"
        )

    user_content = (
        f"{location_context}\n\n"
        f"{vendor_context}\n\n"
        f"Task: {prompt}\n\n"
        f"Analysis: {analysis_summary}\n\n"
        f"Generate a concise, realistic, professional vendor outreach email.\n\n"
        f"Sign the message as:\n"
        f"{config.DEFAULT_USER_NAME}\n"
        f"{config.DEFAULT_COMPANY_NAME}"
    )

    try:
        response = await client.chat.completions.create(
            model=config.OPENAI_MODEL,
            messages=[
                {
                    "role": "system",
                    "content": "You are a professional procurement assistant. Generate extremely concise vendor outreach messages."
                },
                {
                    "role": "user",
                    "content": user_content
                }
            ],
            temperature=config.OPENAI_TEMPERATURE,
            max_tokens=150
        )
    except Exception as e:
        logger.exception("OpenAI request failed during draft generation")
        raise
    draft = response.choices[0].message.content.strip()
    
    # Validate LLM output
    if not draft:
        logger.warning("LLM returned empty draft output")
        raise ValueError("Empty draft generated by LLM")
    
    # Defensive truncation
    draft = draft[:MAX_OUTPUT_LENGTH]
    
    logger.info("Draft generation completed")
    return draft

async def self_reflect_draft(draft: str, prompt: str, rejection_feedback: Optional[str] = None) -> str:
    """
    Reviews and improves an outreach message for professionalism, tone, and clarity
    while keeping it concise and aligned with the original task prompt.
    """
    logger.info("Self-reflection started")
    location_context = (
        f"User procurement region:\n"
        f"City: {config.DEFAULT_CITY}\n"
        f"Locality: {config.DEFAULT_LOCALITY}\n"
        f"Pincode: {config.DEFAULT_PINCODE}"
    )

    user_content = (
        f"Original task: {prompt}\n\n"
        f"{location_context}\n\n"
        f"Improve this outreach draft while keeping it concise and professional:\n\n{draft}"
    )
    
    if rejection_feedback:
        user_content = (
            f"Original task: {prompt}\n\n"
            f"{location_context}\n\n"
            f"User rejected the previous draft with the following feedback:\n"
            f"'{rejection_feedback}'\n\n"
            f"Regenerate a better outreach draft aligned with this feedback, while keeping it concise and professional:\n\n{draft}"
        )
        
    try:
        response = await client.chat.completions.create(
            model=config.OPENAI_MODEL,
            messages=[
                {
                    "role": "system",
                    "content": (
                        "You are a senior procurement communication reviewer.\n"
                        "Critically evaluate the outreach message for:\n"
                        "* professionalism\n"
                        "* clarity\n"
                        "* persuasion\n"
                        "* specificity\n"
                        "* business tone\n\n"
                        "Then rewrite the message into a significantly stronger, "
                        "more realistic, business-ready outreach while keeping it concise."
                    )
                },
                {
                    "role": "user",
                    "content": user_content
                }
            ],
            temperature=config.REFLECTION_TEMPERATURE,
            max_tokens=150
        )
    except Exception as e:
        logger.exception("OpenAI request failed during self-reflection")
        raise
    improved = response.choices[0].message.content.strip()
    
    # Validate LLM output
    if not improved:
        logger.warning("LLM returned empty self-reflection output")
        raise ValueError("Empty self-reflection output generated by LLM")
    
    # Defensive truncation
    improved = improved[:MAX_OUTPUT_LENGTH]
    
    logger.info("Self-reflection completed")
    return improved
