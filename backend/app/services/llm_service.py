import json
import re
from typing import Optional

from openai import AsyncOpenAI

from app.core import config
from app.core.logger import get_logger
from app.utils.retry import retry_with_backoff

logger = get_logger("services.llm")

# Maximum allowed output length for defensive truncation
MAX_OUTPUT_LENGTH = 2000

# ---------------------------------------------------------------------------
# Provider-aware client & model helpers
# ---------------------------------------------------------------------------

def _get_client() -> AsyncOpenAI:
    """
    Returns the appropriate AsyncOpenAI client based on AGENT_PROVIDER.

    - For "openai": uses OPENAI_API_KEY (standard OpenAI endpoint).
    - For "ollama": points base_url at the local Ollama OpenAI-compatible server.
    """
    if config.AGENT_PROVIDER == "ollama":
        # Ollama's OpenAI-compatible endpoint; api_key may be empty or a
        # user-configured key if the Ollama setup requires one.
        return AsyncOpenAI(
            api_key=config.OPENAI_API_KEY or "ollama",
            base_url=config.OLLAMA_BASE_URL,
        )
    # Default: standard OpenAI
    return AsyncOpenAI(api_key=config.OPENAI_API_KEY)


def _get_model() -> str:
    """Returns the model name for the currently configured AGENT_PROVIDER."""
    if config.AGENT_PROVIDER == "ollama":
        return config.OLLAMA_MODEL
    return config.OPENAI_MODEL


# Shared client instance – created lazily so config is fully resolved.
_client: Optional[AsyncOpenAI] = None


def get_client() -> AsyncOpenAI:
    global _client
    if _client is None:
        _client = _get_client()
    return _client


# ---------------------------------------------------------------------------
# Token usage logging helper
# ---------------------------------------------------------------------------

def _log_llm_used(step_name: str) -> None:
    """Log a clear 'llm used' message with provider and model name."""
    logger.info(
        "llm used : %s (%s provider)",
        _get_model(),
        config.AGENT_PROVIDER,
        extra={
            "llm_step": step_name,
            "model": _get_model(),
            "provider": config.AGENT_PROVIDER,
        },
    )


def _log_usage(step_name: str, response) -> None:
    """Log model name and token usage from an OpenAI-compatible response."""
    usage = getattr(response, "usage", None)
    if usage:
        logger.info(
            "Step=%s Model=%s PromptTokens=%s CompletionTokens=%s TotalTokens=%s",
            step_name,
            getattr(response, "model", _get_model()),
            getattr(usage, "prompt_tokens", "?"),
            getattr(usage, "completion_tokens", "?"),
            getattr(usage, "total_tokens", "?"),
            extra={
                "llm_step": step_name,
                "model": getattr(response, "model", _get_model()),
                "prompt_tokens": getattr(usage, "prompt_tokens", None),
                "completion_tokens": getattr(usage, "completion_tokens", None),
                "total_tokens": getattr(usage, "total_tokens", None),
            },
        )
    else:
        # Ollama may not return usage info in all configurations
        logger.info(
            "Step=%s Model=%s (no token usage reported by provider)",
            step_name,
            _get_model(),
        )


# ---------------------------------------------------------------------------
# Shared helper: safe LLM chat completion call
# ---------------------------------------------------------------------------

@retry_with_backoff()
async def _llm_call(
    step_name: str,
    system_prompt: str,
    user_content: str,
    temperature: float = config.OPENAI_TEMPERATURE,
    max_tokens: int = 500,
) -> str:
    """Makes an LLM chat completion call and returns the content string."""
    _log_llm_used(step_name)
    try:
        response = await get_client().chat.completions.create(
            model=_get_model(),
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_content},
            ],
            temperature=temperature,
            max_tokens=max_tokens,
        )
    except Exception as e:
        logger.exception("LLM request failed during %s (provider=%s)", step_name, config.AGENT_PROVIDER)
        raise
    _log_usage(step_name, response)
    text = response.choices[0].message.content.strip()
    if not text:
        logger.warning("LLM returned empty output for %s", step_name)
        raise ValueError(f"Empty output generated by LLM for {step_name}")
    return text[:MAX_OUTPUT_LENGTH]


async def generate_execution_result(prompt: str, improved_draft: Optional[str], draft: Optional[str]) -> str:
    """
    Uses LLM to generate the final execution result after approval.
    Produces a realistic delivery confirmation relevant to the topic.
    """
    logger.info("Execution result generation started (provider=%s, model=%s)", config.AGENT_PROVIDER, _get_model())

    final_draft = improved_draft or draft or ""

    system_prompt = (
        "You are a workflow execution system. Generate a brief execution result "
        "confirming that the approved outreach has been delivered. "
        "Be realistic, professional, and relevant to the original task topic."
    )

    user_content = (
        f"Task: {prompt}\n\n"
        f"Outreach message:\n{final_draft}\n\n"
        f"Generate a brief execution confirmation result describing the delivery outcome."
    )

    try:
        result = await _llm_call("generate_execution_result", system_prompt, user_content, max_tokens=200)
        logger.info("Execution result generation completed")
        return result
    except (ValueError, Exception) as e:
        logger.warning("LLM execution result generation failed, using fallback: %s", e)
        return f"Approved workflow executed successfully for task: {prompt[:100]}..."


